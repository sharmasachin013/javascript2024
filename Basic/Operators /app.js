// Addition
let addResult = 5 + 3;
console.log("Addition: 5 + 3 =", addResult); // 8

// Subtraction
let subResult = 10 - 4;
console.log("Subtraction: 10 - 4 =", subResult); // 6

// Multiplication
let mulResult = 7 * 6;
console.log("Multiplication: 7 * 6 =", mulResult); // 42

// Division
let divResult = 20 / 4;
console.log("Division: 20 / 4 =", divResult); // 5

// Modulus
let modResult = 22 % 5;
console.log("Modulus: 22 % 5 =", modResult); // 2

// Exponentiation
let expResult = 3 ** 4;
console.log("Exponentiation: 3 ** 4 =", expResult); // 81

// Increment
let incValue = 5;
incValue++;
console.log("Increment: 5++ =", incValue); // 6

// Decrement
let decValue = 10;
decValue--;
console.log("Decrement: 10-- =", decValue); // 9
